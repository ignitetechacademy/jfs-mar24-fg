package com.syam.strings;

public class StringImmutability {

	public static void main(String[] args) {
		
		String company = "Ignite Tech";
		System.out.println(company);
		
		company = "Ignite Tech Academy";
		System.out.println(company);
		
		String org = "Ignite Tech";
		String institute = "Ignite Tech";
		
		org = "Ignite, BAngalore";
		
	}
}
